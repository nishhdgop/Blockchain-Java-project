package gui;

public class cryptoExecption extends Exception {
	

	   public cryptoExecption ()
	   {
	      super ();
	   }

	   public cryptoExecption (String message)
	   {
	      super (message);
	   }

	   public cryptoExecption (Throwable cause)
	   {
	      super (cause);
	   }

	   public cryptoExecption (String message, Throwable cause)
	   {
	      super (message, cause);
	   }
	}

